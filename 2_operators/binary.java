package operators;

public class binary {
    public static void main(String[] args) {
    
        int a = 10,b=5;
        System.out.println("a + b = "+(a+b));
        System.out.println("a - b = "+(a-b));
        System.out.println("a * b = "+(a*b));
        System.out.println("a / b = "+(a/b));
        System.out.println("a % b = "+(a%b));
    }
}
// Arithmetic operators are the building blocks of mathematical computations!
// Fun fact: The modulo operator (%) gives the remainder of a division operation!
