/*
 * File: PrintHello.java
 * Description: This program demonstrates function creation and calling with a simple hello function.
 * Author: Satyam Raj
 * Date: October 2025
 * 
 * Purpose: This program demonstrates how to:
 * - Create a static method (function) in Java
 * - Call a function from the main method
 * - Understand the basic structure of method definition
 * - See how functions help reduce code repetition
 * 
 * The hello() function prints "Hello, World!" four times,
 * and is called once from the main method.
 */
public class printhello {
    public static void hello() {
        System.out.println("Hello, World!");
        System.out.println("Hello, World!");
        System.out.println("Hello, World!");
        System.out.println("Hello, World!");
    }

    public static void main(String[] args) {
        hello(); // this is function call
    }

}
// Functions: The building blocks of modular programming!
// Fun fact: Functions allow you to write code once and reuse it multiple times!
